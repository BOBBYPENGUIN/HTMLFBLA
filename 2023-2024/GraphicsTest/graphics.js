Graphics.prototype.getColors = function(){
    if(this.type == 1){
        return this.drawWhiteSquare();
    } else if(this.type == 2){
        return this.drawPlayerHead();
    } else if(this.type == 3){
        return this.drawTorso();
    } else if(this.type == 4){
        return this.drawLegs();
    } else if(this.type == 5){
        return this.drawGrass();
    }
}
Graphics.prototype.getCoordinates = function(){
    var positions = []
    for(var column = 0; column < 16; column++){
        var rowContents = []
        for(var row = 0; row < 16; row++){
            rowContents.push([this.xPos + row * this.scale, this.yPos + column * this.scale])
        }
        positions.push(rowContents);
    }
    return positions;
}
Graphics.prototype.move = function(dx, dy){
    this.xPos = this.xPos + dx;
    this.yPos = this.yPos + dy;
}
